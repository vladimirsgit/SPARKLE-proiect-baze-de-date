    CREATE TABLE users (
        user_id NUMBER(6, 0) GENERATED BY DEFAULT AS IDENTITY,
        username VARCHAR(30) NOT NULL,
        password VARCHAR(50) NOT NULL,
        firstName VARCHAR(30) NOT NULL,
        lastName VARCHAR(30) NOT NULL,
        email VARCHAR(100) NOT NULL,
        created TIMESTAMP DEFAULT SYSTIMESTAMP,
        modified TIMESTAMP,
        PRIMARY KEY (user_id)
    );

    CREATE TABLE regions(
        region_id NUMBER(1, 0) GENERATED BY DEFAULT AS IDENTITY,
        name VARCHAR(30) NOT NULL,
        PRIMARY KEY (region_id)
    );

    CREATE TABLE countries(
        country_id CHAR(2 BYTE),
        region_id NUMBER(1, 0),
        name VARCHAR(40) NOT NULL,
        PRIMARY KEY (country_id),
        CONSTRAINT fk_region_id
            FOREIGN KEY(region_id)
                REFERENCES regions(region_id)
    );

    CREATE TABLE user_address(
        user_address_id NUMBER(6, 0) GENERATED BY DEFAULT AS IDENTITY,
        user_id NUMBER(6, 0),
        country_id char(2 BYTE),
        "state/province" VARCHAR(50) NOT NULL,
        city VARCHAR(50) NOT NULL,
        street VARCHAR(100) NOT NULL,
        zip VARCHAR(20),
        phone VARCHAR(30),
        PRIMARY KEY(user_address_id),
        CONSTRAINT fk_user_id 
            FOREIGN KEY(user_id)
                REFERENCES users(user_id),
        CONSTRAINT fk_country_id
            FOREIGN KEY(country_id)
                REFERENCES countries(country_id)
    );

    CREATE TABLE user_payment(
        user_payment_id NUMBER(6, 0) GENERATED BY DEFAULT AS IDENTITY,
        user_id NUMBER(6, 0),
        payment_type VARCHAR(20) NOT NULL,
        account_no VARCHAR(30),
        PRIMARY KEY(user_payment_id),
        CONSTRAINT fk_user_payment_user_id
            FOREIGN KEY(user_id)
                REFERENCES users(user_id)
    );

    CREATE TABLE genres(
        genre_id NUMBER(2, 0) GENERATED BY DEFAULT AS IDENTITY,
        name VARCHAR(30) NOT NULL,
        PRIMARY KEY (genre_id)
    );

    CREATE TABLE publishers(
        publisher_id NUMBER(4, 0) GENERATED BY DEFAULT AS IDENTITY,
        name VARCHAR(50) NOT NULL,
        email VARCHAR(100) NOT NULL,
        phone VARCHAR(20),
        PRIMARY KEY (publisher_id)
    );

    CREATE TABLE product_type(
        product_type_id CHAR(3 BYTE),
        name VARCHAR(20),
        PRIMARY KEY(product_type_id)
    );

    CREATE TABLE products(
        product_id NUMBER(6, 0) GENERATED BY DEFAULT  AS IDENTITY,
        product_type_id CHAR(3 BYTE),
        publisher_id NUMBER(4, 0),
        name VARCHAR(100) NOT NULL,
        price NUMBER(5, 2) NOT NULL,
        PRIMARY KEY (product_id),
        CONSTRAINT fk_products_product_type_id
                        FOREIGN KEY(product_type_id)
                        REFERENCES PRODUCT_TYPE(product_type_id),
        CONSTRAINT fk_products_publisher_id
                        FOREIGN KEY (publisher_id)
                        REFERENCES PUBLISHERS(publisher_id)
    );

    CREATE TABLE product_genres(
        product_id NUMBER(6, 0),
        genre_id NUMBER(2, 0),
        PRIMARY KEY(product_id, genre_id),
        CONSTRAINT fk_product_genres_product_id
                            FOREIGN KEY(product_id)
                            REFERENCES PRODUCTS(product_id),
        CONSTRAINT fk_product_genres_genre_id
                            FOREIGN KEY(genre_id)
                            REFERENCES GENRES(genre_id)
    );

    CREATE TABLE orders(
        order_id NUMBER(6, 0) GENERATED BY DEFAULT AS IDENTITY,
        user_id NUMBER(6, 0),
        order_date TIMESTAMP DEFAULT SYSTIMESTAMP,
        PRIMARY KEY (order_id),
        CONSTRAINT fk_orders_user_id
                    FOREIGN KEY (user_id)
                    REFERENCES USERS(user_id)
    );

    CREATE TABLE product_from_order(
        order_id NUMBER(6, 0),
        product_id NUMBER(6, 0),
        quantity NUMBER(3, 0),
        amount NUMBER(6, 2),
        PRIMARY KEY (order_id, product_id),
        CONSTRAINT fk_product_from_order_order_id
                                FOREIGN KEY (order_id)
                                REFERENCES ORDERS(order_id),
        CONSTRAINT fk_product_from_order_product_id
                                FOREIGN KEY (product_id)
                                REFERENCES PRODUCTS(product_id)
                                
    );

    CREATE TABLE reviews(
    user_id NUMBER(6, 0),
    product_id NUMBER(6, 0),
    opinion VARCHAR(2000),
    rating NUMBER(1, 0) NOT NULL,
    PRIMARY KEY (user_id, product_id),
    CONSTRAINT fk_reviews_user_id
                    FOREIGN KEY (user_id)
                    REFERENCES USERS(user_id),
    CONSTRAINT fk_reviews_product_id
                    FOREIGN KEY (product_id)
                    REFERENCES PRODUCTS(product_id)
);

